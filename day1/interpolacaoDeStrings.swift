/*
interpolação de strings é a forma de injetar dados 
personalizados em strings em tempo de execução: substitui uma ou mais 
partes de uma string por dados fornecidos por nós.
*/

var city = "Pinhal Novo"
var mensagem = "Welcome to \(city)"
print(mensagem)

// ou 
var cidade = "Lisboa"
print("Bem vindo a \(cidade)")

